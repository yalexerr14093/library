function seedsForGenre(genre) {
  const compact = String(genre).replace(/\s+/g, "");
  return GENRE_TOPIC_SEEDS[compact] || GENRE_TOPIC_SEEDS[genre] || null;
}

async function countInGenre(client, genre) {
  const { rows } = await client.query("SELECT COUNT(*)::int AS c FROM books WHERE genre = $1", [genre]);
  return rows[0].c;
}

async function bookExists(client, title, author) {
  const { rows } = await client.query(
    `SELECT 1 FROM books
     WHERE lower(btrim(title)) = lower(btrim($1)) AND lower(btrim(author)) = lower(btrim($2))
     LIMIT 1`,
    [title, author]
  );
  return rows.length > 0;
}

async function insertWithCover(client, genre, title, author, year, description) {
  if (await bookExists(client, title, author)) return false;
  const raw = await fetchCoverFromGoogle(title, author);
  if (!raw) return false;
  const coverUrl = normalizeCoverUrl(raw);
  if (!coverUrl) return false;
  if (await bookExists(client, title, author)) return false;
  try {
    await client.query(
      `INSERT INTO books (title, author, genre, year, cover_emoji, description, cover_url)
       VALUES ($1, $2, $3, $4, $5, $6, $7)`,
      [title, author, genre, year, "📖", description, coverUrl]
    );
  } catch (e) {
    if (e?.code === "23505") return false;
    throw e;
  }
  return true;
}

/**
 * @param {import("pg").PoolClient} client — активная транзакция initDb
 */
async function normalizeSparseGenres(client) {
  const sparseRes = await client.query(
    `SELECT genre FROM books GROUP BY genre HAVING COUNT(*) < $1`,
    [MIN_BOOKS_PER_GENRE]
  );
  const sparse = sparseRes.rows.map((r) => r.genre);
  if (!sparse.length) return;

  for (const genre of sparse) {
    if (genre === FALLBACK_GENRE) continue;
    const seeds = seedsForGenre(genre);
    if (!seeds || !seeds.length) continue;

    let c = await countInGenre(client, genre);
    if (c >= MIN_BOOKS_PER_GENRE) continue;

    for (const row of seeds) {
      if (c >= MIN_BOOKS_PER_GENRE) break;
      const [title, author, year, description] = row;
      if (await bookExists(client, title, author)) continue;
      try {
        const ok = await insertWithCover(client, genre, title, author, year, description);
        if (ok) {
          c++;
          await sleep(500);
        }
      } catch (e) {
        console.warn(`genreBalance: пропуск «${title}» (${genre}):`, e?.message || e);
      }
    }
  }

  const stillSparse = await client.query(
    `SELECT genre FROM books GROUP BY genre HAVING COUNT(*) < $1`,
    [MIN_BOOKS_PER_GENRE]
  );

  for (const { genre } of stillSparse.rows) {
    if (genre === FALLBACK_GENRE) continue;
    const u = await client.query("UPDATE books SET genre = $1 WHERE genre = $2", [FALLBACK_GENRE, genre]);
    if (u.rowCount > 0) {
      console.log(`📚 Жанр «${genre}» объединён с «${FALLBACK_GENRE}» (${u.rowCount} книг)`);
    }
  }

  const fallbackCount = await countInGenre(client, FALLBACK_GENRE);
  if (fallbackCount < MIN_BOOKS_PER_GENRE) {
    console.warn(
      `genreBalance: в «${FALLBACK_GENRE}» мало книг (${fallbackCount}) — проверьте каталог вручную.`
    );
  }
}

/** Отдельная короткая транзакция (после initDb), чтобы не держать блокировки во время HTTP к Google Books */
async function runGenreBalance(pool) {
  const c = await pool.connect();
  try {
    await c.query("BEGIN");
    await normalizeSparseGenres(c);
    await c.query("COMMIT");
  } catch (e) {
    await c.query("ROLLBACK");
    console.error("genreBalance: ошибка:", e?.message || e);
    throw e;
  } finally {
    c.release();
  }
}

